struct  {
    
    bool operator()(int a, int b)
    {
        return a < b;
    }
    
}CompareLess;

struct  {
    
    bool operator()(int a, int b)
    {
        return  a > b;
    }
    
}CompareMore;

bool compare(int a, int b)
{
    return a < b;
}

using namespace std;

class RoyalTreasurer {
    public:
    int minimalArrangement(vector<int> A, vector<int> B) {
        
        sort(A.begin(),A.end(),CompareLess);
        sort(B.begin(),B.end(),CompareMore);
        
        int sum = 0 ;
        for ( int i = 0 ; i < A.size(); i++)
        {
            sum += A[i]*B[i];
        }
        return sum;
    }
};

