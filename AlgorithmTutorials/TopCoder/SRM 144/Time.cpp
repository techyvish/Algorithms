class Time {
    public:
    std::string whatTime(int seconds)
    {
        std::stringstream result;
        if ( !seconds ){
            result << 0 << ":" << 0 << ":" << 0;
            return result.str();
        }
        int secs = seconds % 60 ;
        int reminder = seconds / 60;
        int mins = reminder % 60 ;
        int hours = reminder / 60;
        result << hours << ":" << mins << ":" << secs;
        return result.str();
    }
};

